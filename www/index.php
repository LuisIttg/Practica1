<p class="text-danger">¿QUÉ ES FRAMEWORKS?</p>
<br>
<p class="text-dark">El framework se refiere a una estructura básica subyacente en la ingeniería de software, <br>que sirve al programador como estructura de desarrollo para su programación de aplicaciones. <br>Los frameworks se utilizan principalmente en el desarrollo de software orientado a objetos <br>y en enfoques de desarrollo basados en componentes.</p><br><br>

<p class="text-danger">TIPOS DE ESTRUCTURA: </p>
<br>
<li>Frameworks de aplicación:<br>
Estos frameworks forman una estructura básica de programador para ciertos tipos de aplicaciones. <br>Proporcionan una función y una estructura que son importantes para todas las aplicaciones de ese tipo.</li><br>
<li>Frameworks de dominio:<br>
Los marcos de dominio crean la estructura de programación para un área problemática particular <br>y por lo tanto proporcionan funciones para resolver este problema.</li><br>
<li>Frameworks de clase:<br>
Framework son una combinación de clases y métodos que se pueden utilizar para una <br>amplia gama de aplicaciones. Apoyan la implementación de la aplicación del programador a un cierto nivel abstracto.</li><br>
<li>Frameworks de componentes:<br>
Estos frameworks de trabajo proporcionan un entorno para el desarrollo e integración <br>de componentes de software, que son un conjunto de clases y generalmente tienen una interfaz claramente definida.</li><br>
<li>Frameworks de coordinación:<br>
Estos frameworks proporcionan la capacidad de configurar interacciones de dispositivos <br>y sirven para garantizar una compatibilidad perfecta.</li><br>
<li>Frameworks de prueba:<br>
Como su nombre lo indica, este framework se utiliza para probar software desarrollado. <br>Ejemplos bien conocidos son JUnit para pruebas de módulos y Selenium para pruebas de aplicaciones web.</li><br>
<li>Frameworks de web:<br>
Los frameworks web están diseñados para el desarrollo de webs dinámicss y aplicaciones web. <br>En este sentido, proporcionan métodos y funciones para apoyar a los desarrolladores.</li>
